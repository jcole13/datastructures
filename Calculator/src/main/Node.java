package main;

public class Node {
	private Node next;
	private String data;
	
	public Node(String s){
		data = s;
		next = null;
	}
	
	public Node(){
		next = null;
		data = null;
	}
	public Node(String s, Node n){
		data = s;
		next = n;
	}
	
	public Node getNext(){
		return next;
	}
	
	public void setNext(Node n){
		next = n;
	}
	
	public String getData(){
		return data;
	}
	
	public void setData(String s){
		data = s;
	}

}
